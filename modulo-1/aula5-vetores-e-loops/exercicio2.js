let capitais = ["Salvador", "Aracajú", "Maceió", "Recife", "João Pessoa"];

/*Fazendo com While*/
let indice = 0;
while (indice < capitais.length) {
    console.log(capitais[indice]);
    indice++;
}

/*Fazendo com FOR*/
let numerosPares = [0, 2, 4, 6, 8]
for (let i = 0; i < numerosPares.length; i++) {
    console.log(numerosPares[i]);
}